;-----------------------------------------------------------------------------
; 텍스트 모드로 전환
;-----------------------------------------------------------------------------
text_mode:
    mov ah, 0x00
    mov al, TEXT_MODE_NUM
    int 0x10
    ret
    
;-----------------------------------------------------------------------------
; 그래픽 모드로 전환
;-----------------------------------------------------------------------------
graphic_mode:
    mov ah, 0x00
    mov al, GRAPHIC_MODE_NUM
    int 0x10
    ret

TEXT_MODE_NUM equ 0x03
GRAPHIC_MODE_NUM equ 0x13
